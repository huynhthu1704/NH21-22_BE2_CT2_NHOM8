    public function store(Request $request)
    {
        $banner = new Banner;
        $banner->title = $request->title;
        $banner->content = $request->content;
        $banner->category_id = $request->category;
        $banner->position = $request->position;
        $banner->pageName = "index";
        $target_dir = "uploads/";
